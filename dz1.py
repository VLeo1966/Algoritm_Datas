# Шаг 1: Привести строку к нижнему регистру, чтобы игнорировать различия между прописными и строчными буквами.
# Шаг 2: Удалить из строки все пробелы, пунктуацию и другие ненужные символы, чтобы проверить только буквы и цифры.
# Шаг 3: Сравнить строку с ее обратной версией.
# Шаг 4: Если строка и ее обратная версия совпадают, вернуть True (строка является палиндромом).
# Шаг 5: Если не совпадают, вернуть False (строка не является палиндромом).

import re

# Функция использует регулярное выражение для удаления всех символов,
# кроме букв и цифр, и затем проверяет, является ли строка палиндромом.
def is_palindrome(s: str) -> bool:
    # Привести строку к нижнему регистру
    s = s.lower()

    # Удалить все ненужные символы, оставив только буквы и цифры
    s = re.sub(r'[^a-z0-9]', '', s)

    # Сравнить строку с ее обратной версией
    return s == s[::-1]


# Пример использования функции
print(is_palindrome("A man, a plan, a canal, Panama"))  # True
print(is_palindrome("880088"))  # True
print(is_palindrome("Hello, World!"))  # False
